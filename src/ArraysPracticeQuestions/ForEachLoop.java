package ArraysPracticeQuestions;

public class ForEachLoop {

	public static void main(String[] args) {
		
		String arr[] = { "Hello", "World", "Geeks", "For", "Geeks" };
		
		String a[]= {"Legends"};
		
		for(String e:arr) {
			System.out.println(e);
		}
		
		for(String e:a) {
			System.out.println(e);
		}
		
	}

}
